<style>
.modal-bg {
  background-color: rgba(0, 0, 0, 0.5);
  z-index: -9999;
}
</style>

<button id="openBtn">Open Modal</button>

<template>
  <div
    id="modal"
    class="modal-bg transition-opacity duration-500 opacity-0 pt-16 fixed w-full h-full left-0 z-10 overflow-auto"
  >
    <div class="modal-content relative m-auto bg-gray-100 w-4/5 shadow-lg">
      <div class="p-4 bg-gray-700 text-white">
        <span
          class="closeBtn float-right text-lg font-bold hover:text-gray-500 no-underline cursor-pointer"
          >&times;</span
        >
        <h2>Modal Header</h2>
      </div>
      <div class="p-4">
        <p>Some text in the Modal Body</p>
        <p>Some other text...</p>
      </div>
      <div class="p-4 text-white bg-gray-500">
        <h3>Modal Footer</h3>
      </div>
    </div>
  </div>
</template>

<script>
var modal = document.querySelector("#modal");
var openBtn = document.querySelector("#openBtn");
var closeBtn = document.querySelector(".closeBtn");

openBtn.onclick = function () {
  modal.classList.add("opacity-100");
  modal.classList.add("z-50");
};

closeBtn.onclick = function () {
  modal.classList.remove("opacity-100");
  modal.classList.remove("z-50");
};

window.onclick = function (event) {
  if (event.target == modal) {
    modal.classList.remove("opacity-100");
    modal.classList.remove("z-50");
  }
};
</script>
